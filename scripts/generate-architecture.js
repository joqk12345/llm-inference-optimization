const { chapterRefFromTitle, formatMarkdownTable, loadControlPlane, replaceAutoGeneratedBlock, writeText, loadText } = require('./knowledge-lib.js');

const graph = JSON.parse(loadText('generated/graph.json'));
const { architecture } = loadControlPlane();
const coreNodes = graph.nodes
  .filter((node) => node.file && node.metadata?.source_scope === 'core')
  .sort((left, right) => left.metadata.display_order - right.metadata.display_order);

const layerRows = architecture.layers.map((layer) => {
  const layerNodes = coreNodes.filter((node) => node.metadata.architecture_layer.includes(layer.name));
  const chapters = layerNodes.map((node) => chapterRefFromTitle(node.metadata.title)).join('、') || '—';
  return [layer.name, chapters, layer.description];
});

const layerNames = architecture.layers.map((layer) => layer.name);
const matrixRows = coreNodes.map((node) => [
  chapterRefFromTitle(node.metadata.title),
  ...layerNames.map((layer) => (node.metadata.architecture_layer.includes(layer) ? '●' : ''))
]);

const lines = [
  '# Generated Architecture Map',
  '',
  '> Auto-generated from `architecture_layer` metadata on core reading-path content.',
  '',
  '## 分层说明',
  '',
  ...formatMarkdownTable(['架构层', '章节', '说明'], layerRows),
  '',
  '## 章节分层矩阵',
  '',
  ...formatMarkdownTable(['章节', ...layerNames], matrixRows)
];

writeText('generated/architecture.md', `${lines.join('\n')}\n`);

replaceAutoGeneratedBlock('visualizations/architecture-overview.md', 'ARCHITECTURE', [
  '## 自动生成：分层说明',
  '',
  ...formatMarkdownTable(['架构层', '章节', '说明'], layerRows),
  '',
  '## 自动生成：章节分层矩阵',
  '',
  ...formatMarkdownTable(['章节', ...layerNames], matrixRows)
]);

console.log('generated/architecture.md created');
